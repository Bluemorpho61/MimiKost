    private void jTable_nmPenyewaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_nmPenyewaMouseClicked
        // TODO add your handling code here:
        int row = jTable_nmPenyewa.getSelectedRow();
        try {
            
          //  int row=jTable_nama.getSelectedRow();
        String nik =jTable_nmPenyewa.getValueAt(row, 0).toString();
        String sql="SELECT foto FROM tb_penyewa WHERE kode_ktp='"+nik+"'";
        Connection c =(Connection)mimikostswing.Config.configDB();
        Statement st = c.prepareStatement(sql);
        ResultSet r =st.executeQuery(sql);
            if (r.next()) {
                BufferedImage im = ImageIO.read(r.getBinaryStream("foto"));
                ImageIcon imc = new ImageIcon(im);
                Image img = imc.getImage();
                Image setIM = img.getScaledInstance(jLabel_foto.getWidth(), jLabel_foto.getHeight(), Image.SCALE_SMOOTH);
                jLabel_foto.setIcon(new ImageIcon(setIM));
                jLabel_foto.setText(null);
     
            }
        } catch(Exception e){
            
        }
    }//GEN-LAST:event_jTable_nmPenyewaMouseClicked

