     public void TablePenyewaAturKamar(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("No. KTP");
        tbl.addColumn("Nama");
        tbl.addColumn("Blok");
        tbl.addColumn("Kamar");
        jTable_nmPenyewa.setModel(tbl);
        try {
            String sql="SELECT tb_penyewa.kode_ktp, tb_penyewa.nama_penyewa, tb_blok.kode_blok, tb_kamar.no_kamar FROM tb_blok JOIN tb_kamar ON tb_blok.kode_blok = tb_kamar.kode_blok JOIN tb_penyewa ON tb_penyewa.id_kamar = tb_kamar.id_kamar";
            Statement statement =(Statement)mimikostswing.Konek.getConnection().createStatement();
            ResultSet res = statement.executeQuery(sql);
            while (res.next()) {                
            tbl.addRow(new Object[]{
                res.getString("kode_ktp"),
                res.getString("nama_penyewa"),
                res.getString("kode_blok"),
                res.getString("no_kamar")
                //res.getString("no_kamar"),
            });    
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error2: "+ e.getMessage());
        }
    }

     public void showProfile(){
        try {
            String sql ="SELECT * FROM tb_penyewa WHERE NIK='"+NIK+"'";
            Connection conn=(Connection)Config.configDB();
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet res =pst.executeQuery(sql);
            if (res.next()) {
                BufferedImage im = ImageIO.read(res.getBinaryStream("foto"));
                ImageIcon Imic = new ImageIcon(im);
                Image img = Imic.getImage();
                Image setImage = img.getScaledInstance(jPanel_foto.getWidth(),jPanel_foto .getHeight(), Image.SCALE_SMOOTH);
                jLabel_foto.setIcon(new ImageIcon(setImage));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error1: "+ e.getMessage());
        }
    }
     
      public void showKamar(){
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("id_kamar");
        tb.addColumn("No Kamar");
        jTable_kamar.setModel(tb);
        try {
            String SQL ="SELECT tb_kamar.id_kamar,tb_kamar.no_kamar FROM tb_kamar,tb_blok WHERE tb_blok.kode_blok ='"+jTextField2.getText()+"' AND tb_blok.kode_blok = tb_kamar.kode_blok";
            String SQLAsc="SELECT tb_kamar.id_kamar,tb_kamar.no_kamar FROM tb_kamar,tb_blok WHERE tb_blok.kode_blok ='"+jTextField2.getText()+"' AND tb_blok.kode_blok = tb_kamar.kode_blok ORDER BY tb_kamar.no_kamar ASC";
            Statement stm =(Statement)Konek.getConnection().createStatement();
            ResultSet rs = stm.executeQuery(SQLAsc);
            while(rs.next()){
                tb.addRow(new Object[]{
                    rs.getString("id_kamar"),
                    rs.getString("no_kamar")
                });
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error: "+ e.getMessage());
        }
        
    }
