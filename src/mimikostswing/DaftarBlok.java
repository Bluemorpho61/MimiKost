    public void DataTable1(){
        DefaultTableModel tb = new DefaultTableModel();
        
        
        jTable1.setModel(tb);
        tb.addColumn("Kode Blok");
        tb.addColumn("Jumlah Kamar");
        tb.addColumn("Harga");
        try {
            String sql ="SELECT * FROM tb_blok";
           
            java.sql.Statement stm =(Statement)Konek.getConnection().createStatement();
           
            ResultSet res = stm.executeQuery(sql);
            while (res.next()) {                
                tb.addRow(new Object[]{
                    res.getString("kode_blok"),
                    res.getString("jumlah_kamar"),
                    res.getString("harga")
                });
            }
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        
       
    }
